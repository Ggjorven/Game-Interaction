using System;
using System.Text;
using System.Windows;
using System.Windows.Controls;
using System.Windows.Data;
using System.Windows.Documents;
using System.Windows.Input;
using System.Windows.Media;
using System.Windows.Media.Imaging;
using System.Windows.Media.Media3D;
using System.Windows.Navigation;
using System.Windows.Shapes;

namespace Puzzled
{

    public class Event { }

    ////////////////////////////////////////////////////////////////////////////////////
    // Windows events
    ////////////////////////////////////////////////////////////////////////////////////
    public class WindowResizeEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public WindowResizeEvent(uint width, uint height) { Width = width; Height = height; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public uint Width, Height;

    }

    public class WindowCloseEvent : Event { }

    ////////////////////////////////////////////////////////////////////////////////////
    // Key events
    ////////////////////////////////////////////////////////////////////////////////////
    public class KeyPressedEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public KeyPressedEvent(Key keyCode, bool isRepeat) { KeyCode = keyCode; IsRepeat = isRepeat; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public Key KeyCode;
        public bool IsRepeat;
        
    }

    public class KeyReleasedEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public KeyReleasedEvent(Key keyCode) { KeyCode = keyCode; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public Key KeyCode;

    }

    ////////////////////////////////////////////////////////////////////////////////////
    // Mouse events
    ////////////////////////////////////////////////////////////////////////////////////
    public class MouseMovedEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public MouseMovedEvent(float x, float y) { X = x; Y = y; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public float X;
        public float Y;

    }

    public class MouseScrolledEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public MouseScrolledEvent(float yOffset) { YOffset = yOffset; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public float YOffset;

    }

    public class MouseButtonPressedEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public MouseButtonPressedEvent(MouseButton button) { Button = button; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public MouseButton Button;
    
    }

    public class MouseButtonReleasedEvent : Event
    {

        ////////////////////////////////////////////////////////////////////////////////////
        // Constructor
        ////////////////////////////////////////////////////////////////////////////////////
        public MouseButtonReleasedEvent(MouseButton button) { Button = button; }

        ////////////////////////////////////////////////////////////////////////////////////
        // Variables
        ////////////////////////////////////////////////////////////////////////////////////
        public MouseButton Button;
    
    }

}